// Утилиты для работы с данными компании
export const getCompanyAge = () => {
  const currentYear = new Date().getFullYear();
  return currentYear - companyData.foundedYear;
};

export const getFormattedStats = () => {
  return companyData.stats.map((stat) => ({
    ...stat,
    value: stat.value.replace('8+', `${getCompanyAge()}+`),
  }));
};
